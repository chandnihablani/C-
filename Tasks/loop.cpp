using namespace std;
int main()
{
	
	
	cout<<"print the a to z "<<endl;
	
	
	
	for(char a= 'a'; a<='z'; a++)
	{
		cout<<a;
		cout<< endl;
		
	}
	cout<<"print the reverse order ";
	cout<<endl;
	
	
	for(char b='z';b>='a'; b--)
	{
		cout<<b;
		cout<<endl;
	}
	
	
	
	
	
	
}
